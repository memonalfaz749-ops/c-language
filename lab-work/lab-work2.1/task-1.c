#include<stdio.h>
#include<conio.h>
int main(){
	
	//addition
	int number1=12;
	int number2=6;
	
	int number3=number1+number2;
	
	printf(" Addition of %d and %d is %d",number1,number2,number3);
	
	printf("\n %d + %d = %d",number1,number2,number3);
	
	//substraction
	
	int number4=number1-number2;
	
	printf("\n Substraction of %d and %d is %d",number1,number2,number4);
	printf("\n %d - %d = %d",number1,number2,number4);
	
	//Multiplication
	
	int number5=number1*number2;
	
	printf("\n Multiplication of %d and %d is %d",number1,number2,number5);
	printf("\n %d * %d = %d",number1,number2,number5);
	
	
	//Division
	
	int number6=number1/number2;
	
	printf("\n Division of %d and %d is %d",number1,number2,number6);
	printf("\n %d / %d = %d",number1,number2,number6);
	
	
	//Module 
	
	int number7=number1%number2;
	
	printf("\n Module of %d and %d is %d",number1,number2,number7);
	printf("\n %d %% %d = %d",number1,number2,number7);
	
	
	
	return 0;
}
